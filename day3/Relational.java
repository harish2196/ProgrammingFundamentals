package com.chainsys.day3;

public class Relational {

	public static void main(String[] args) {
		int num1 = 10;
        int num2 = 11;
        //Using Int
        
        System.out.println("num1 == num2: " + (num1 == num2));

        System.out.println("num1 != num2: " + (num1 != num2));  

        System.out.println("num1 > num2: " + (num1 > num2));    

        System.out.println("num1 < num2: " + (num1 < num2));    

        System.out.println("num1 >= num2: " + (num1 >= num2));  

        System.out.println("num1 <= num2: " + (num1 <= num2));  
        
        System.out.println();
        
        //Using Char
        
        char a1='A';
        char a2='B';
         System.out.println("a1 == a2: " + (a1==a2));
         System.out.println("a1 == a2: " + (a1!=a2));
         System.out.println("a1 == a2: " + (a1>a2));
         System.out.println("a1 == a2: " + (a1<a2));
         System.out.println("a1 == a2: " + (a1>=a2));
         System.out.println("a1 == a2: " + (a1<=a2));
	}
}